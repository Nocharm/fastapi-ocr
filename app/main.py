# =====================================================================
# [읽기 순서 3/6] app/main.py
#
# 이 파일의 역할:
#   FastAPI 애플리케이션의 "진입점(entry point)".
#   서버를 실행하면 uvicorn이 이 파일의 `app` 객체를 찾아서 실행한다.
#
# ┌─────────────────────────────────────────────────────────┐
# │  비유: 건물의 "안내 데스크 (로비)"                       │
# │                                                         │
# │  건물(앱)에 들어오는 모든 방문객(요청)이                 │
# │  처음으로 만나는 공간이 로비(main.py)다.                 │
# │                                                         │
# │  로비에서는 직접 업무를 처리하지 않고                    │
# │  "OCR 업무는 3층(ocr 라우터)으로 가세요" 처럼           │
# │  각 부서로 안내(라우터 등록)만 한다.                     │
# └─────────────────────────────────────────────────────────┘
#
# uvicorn이란?
#   FastAPI 앱을 실제로 인터넷에서 요청을 받을 수 있게 실행시켜주는 웹 서버.
#
#   비유: 건물 자체 vs 건물을 운영하는 관리 시스템
#     FastAPI(app) = 건물의 설계도 + 내부 구조
#     uvicorn     = 건물의 전기/보안/출입 관리 시스템
#     uvicorn이 없으면 건물(app)이 있어도 외부에서 들어올 수 없다.
#
#   실행 명령어 분석:
#     uvicorn app.main:app --reload
#     ├─ uvicorn    : 웹 서버 실행
#     ├─ app.main   : app/main.py 파일
#     ├─ :app       : 그 파일 안의 `app` 변수
#     └─ --reload   : 코드 변경 시 서버 자동 재시작 (개발 중에만 사용)
# =====================================================================

from fastapi import FastAPI       # FastAPI 클래스
from app.api.routes import ocr    # OCR 라우터 모듈 (routes/ocr.py)


# FastAPI 앱 인스턴스(객체) 생성
# 이 `app` 변수가 서버 전체를 대표한다.
# uvicorn은 이 변수를 찾아서 실행한다.
#
# title, version : 자동 생성되는 API 문서 페이지(/docs)에 표시되는 정보
#   → 서버 실행 후 http://localhost:8000/docs 에서 확인 가능
#   → FastAPI가 Swagger UI 기반의 문서를 자동으로 만들어줌
app = FastAPI(title="OCR API", version="0.1.0")


# 라우터 등록
# ---------------------------------------------------------------
# 비유: 건물 층별 안내판 등록
#   "OCR 관련 업무(ocr.router)는 /ocr 창구로 가세요"
#   라는 안내판을 로비(app)에 붙이는 것.
#
# - ocr.router   : routes/ocr.py에서 만든 라우터 객체
# - prefix="/ocr": 이 라우터의 모든 경로 앞에 "/ocr"을 자동으로 붙여줌
#                  예) ocr.py의 "/upload" → 실제 경로: "/ocr/upload"
# - tags=["ocr"] : /docs 페이지에서 이 라우터의 그룹 이름을 "ocr"로 표시
# ---------------------------------------------------------------
app.include_router(ocr.router, prefix="/ocr", tags=["ocr"])


# 헬스체크 엔드포인트
# ---------------------------------------------------------------
# 비유: 건물 입구의 "영업 중" 표시등
#   외부에서 "이 서버 지금 살아있어?" 를 확인하는 용도.
#   Docker, 쿠버네티스, 모니터링 도구들이 주기적으로 이 경로를 호출해서
#   서버 상태를 체크한다.
#
# @app.get("/health") : GET 방식으로 /health 경로에 요청이 오면 아래 함수 실행
#   데코레이터(@)가 함수와 URL 경로를 연결해주는 역할을 한다.
#   비유: 초인종(URL)을 누르면 특정 사람(함수)이 나오도록 연결해두는 것
# ---------------------------------------------------------------
@app.get("/health")
def health_check():
    # 딕셔너리를 반환하면 FastAPI가 자동으로 JSON으로 변환해서 응답한다.
    # 파이썬: {"status": "ok"}  →  JSON 응답: {"status": "ok"}
    return {"status": "ok"}
