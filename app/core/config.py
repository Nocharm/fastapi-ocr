# 이 파일의 역할:
#   앱 전체에서 사용하는 설정값을 한 곳에서 관리한다.
#   설정값은 .env 파일에서 읽어오며, 없으면 기본값을 사용한다.
#
# ┌─────────────────────────────────────────────────────────┐
# │  비유: 음식점의 "운영 매뉴얼"                            │
# │                                                         │
# │  "영업시간은 11시~22시, 테이블은 20개,                  │
# │   주방은 2층에 있다"처럼                                 │
# │  가게 운영에 필요한 정보를 한 문서에 모아둔 것.          │
# │                                                         │
# │  직원들은 매번 사장한테 물어보는 대신                    │
# │  이 매뉴얼(settings)을 보면 된다.                       │
# │                                                         │
# │  매뉴얼을 바꾸고 싶으면 .env 파일만 수정하면 되고,      │
# │  코드는 건드리지 않아도 된다.                           │
# └─────────────────────────────────────────────────────────┘
#
# .env 파일이란?
#   환경변수를 저장하는 텍스트 파일.
#   코드에 직접 값을 박아두지 않고 외부 파일로 분리하면
#   개발/운영 환경마다 다른 설정을 쉽게 적용할 수 있다.
#   또한 비밀번호, API 키 같은 민감한 정보를 코드에서 분리할 수 있어
#   깃허브에 올려도 .gitignore로 .env만 제외하면 안전하다.
#
#   비유: 코드는 "조리 레시피", .env는 "재료 비율표"
#     레시피(코드)는 공개해도 되지만
#     비밀 재료 비율표(.env)는 주방 안에서만 보관하는 것.
# =====================================================================

from pydantic_settings import BaseSettings
# pydantic_settings : Pydantic의 확장 기능.
# .env 파일의 내용을 읽어서 클래스 필드에 자동으로 매핑해준다.
#
# 비유: 자동 번역기
#   .env 파일에 UPLOAD_DIR=my_folder 라고 쓰면
#   settings.upload_dir 이 자동으로 "my_folder" 가 된다.
#   직접 파일을 파싱하는 코드를 짤 필요가 없다.


class Settings(BaseSettings):

    # 필드 선언 형식: 변수명: 타입 = 기본값
    # .env에 같은 이름의 항목이 있으면 기본값 대신 .env 값을 사용함
    # (대소문자 구분 없음: UPLOAD_DIR = upload_dir 로 자동 인식)

    upload_dir: str = "uploads"
    # 업로드된 파일을 저장할 폴더 경로
    # .env에 UPLOAD_DIR=my_uploads 라고 쓰면 "my_uploads"로 바뀜

    temp_dir: str = "temp"
    # PDF 처리 시 임시 파일을 저장할 폴더 경로
    # Marker는 파일 경로를 입력으로 받아서 임시 파일이 필요함

    easyocr_languages: list[str] = ["en", "ko"]
    # EasyOCR이 인식할 언어 목록
    # "en" = 영어, "ko" = 한국어
    # 언어를 추가할수록 모델 크기가 커지고 로딩 시간이 늘어남

    class Config:
        # BaseSettings에게 ".env 파일을 읽어라"고 지시하는 내부 설정 클래스
        # 이 선언이 없으면 환경변수만 읽고 .env 파일은 무시한다
        env_file = ".env"


# ---------------------------------------------------------------
# 싱글턴(Singleton) 패턴으로 인스턴스를 딱 1개만 생성
#
# 비유: 회사의 "공용 운영 매뉴얼"
#   매뉴얼을 직원마다 따로 출력하는 게 아니라
#   사무실에 1권만 두고 다 같이 돌려보는 것.
#   모두 같은 정보를 보므로 불일치가 생기지 않는다.
#
# 다른 파일에서 사용법:
#   from app.core.config import settings
#   print(settings.temp_dir)  # "temp" 출력
# ---------------------------------------------------------------
settings = Settings()
