#
# 이 파일의 역할:
#   API가 주고받는 데이터의 "모양(구조)"을 미리 정의한다.
#   FastAPI는 이 정의를 보고 응답 데이터를 자동으로 검증하고 JSON으로 변환한다.
#
# ┌─────────────────────────────────────────────────────────┐
# │  비유: 택배 송장 양식                                    │
# │                                                         │
# │  택배를 보낼 때 "받는 사람", "주소", "연락처"를          │
# │  반드시 적어야 하는 정해진 양식이 있다.                  │
# │                                                         │
# │  Schema도 마찬가지로                                    │
# │  "응답 데이터는 반드시 filename과 markdown을             │
# │   가져야 한다"는 양식을 정의해두는 것이다.              │
# │                                                         │
# │  양식에 맞지 않으면(타입이 틀리면) Pydantic이            │
# │  자동으로 에러를 발생시킨다.                             │
# └─────────────────────────────────────────────────────────┘
#
# Pydantic이란?
#   파이썬의 데이터 검증 라이브러리.
#   클래스 필드에 타입(str, int 등)을 선언해두면
#   잘못된 타입이 들어올 때 자동으로 에러를 발생시킨다.
#
#   비유: 엄격한 서류 심사관
#     "이름 칸에 숫자가 들어왔네요? 반려합니다."
#     처럼 데이터 형식이 틀리면 자동으로 걸러준다.
# =====================================================================

from pydantic import BaseModel
# BaseModel : Pydantic의 기본 클래스.
# 이걸 상속(: BaseModel)하면 자동 검증 기능이 활성화된다.
# 비유: 양식지의 "공식 도장". 이 도장이 찍혀야 공식 양식으로 인정됨.


class OCRResponse(BaseModel):
    # 이 클래스가 API의 응답 데이터 구조를 정의한다.
    # routes/ocr.py에서 response_model=OCRResponse 로 사용됨.
    #
    # 클라이언트는 아래 구조의 JSON을 응답으로 받게 된다:
    # {
    #   "filename": "document.pdf",
    #   "markdown": "## 제목\n\n본문 내용..."
    # }

    filename: str   # 업로드된 파일의 이름. 반드시 문자열이어야 함.
                    # 예) "invoice.pdf", "photo.jpg"

    markdown: str   # OCR로 추출된 텍스트 (마크다운 형식).
                    # 예) "## 제목\n\n본문 텍스트..."
